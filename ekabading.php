        <h1 class="article-title">A Butterfly learned how to fly</h1>

        <div class="section">
            <h2>Learning to Be Me: A Story of Waiting, Fear, and Freedom</h2>
            <p>For a long time, I knew something about myself that I couldn't say out loud. I didn't always have the language for it, but I knew that the way my family saw me wasn't the way I saw myself. Growing up, expressing anything outside of what was expected felt dangerous. My brothers were strict, and my family made it very clear what was "allowed" and what wasn't. Every time I tried to be myself, I was reminded that it wasn't acceptable. So I learned to hide.</p>
        </div>

        <div class="section">
            <h2>Living in the Closet</h2>
            <p>I stayed in the closet for years not because I was unsure of who I was, but because I was scared. I was scared of disappointing my family, of being judged, or of being pushed away. Hiding felt safer than telling the truth. I learned how to act in ways that made everyone else comfortable, even though it slowly wore me down. I smiled when I didn't feel happy. I stayed quiet when I wanted to explain myself. I became really good at pretending, and really bad at taking care of myself.</p>
        </div>

        <div class="section">
            <h2>Who I Was When No One Was Watching</h2>
            <p>When I was alone, things felt different. Late at night, when the house was quiet, I could finally be honest with myself. That was when I felt most real. I'd think about who I truly was and wonder if I'd ever be able to live openly as myself. Some nights I felt hopeful. Other nights I felt trapped and angry that being myself had to feel like a risk. Those moments reminded me that I wasn't broken or confused—I was just hiding.</p>
        </div>

        <div class="section">
            <h2>Choosing Myself</h2>
            <p>Coming out wasn't one big moment. It was slow and scary. I waited until the pain of hiding became heavier than the fear of being rejected. Even then, it wasn't easy. Some of my family didn't understand. Some still don't. That hurts, and it's something I'm still learning to live with. But choosing to be honest with myself changed everything.</p>
        </div>

        <div class="section">
            <h2>Finally Free</h2>
            <p>I feel lighter now. I feel real. When I look in the mirror, I don't feel like I'm lying anymore. I didn't change who I was—I just stopped pretending to be someone I wasn't. I spent a long time waiting to be free. Now, even with the struggles, I finally am. And that freedom means everything.</p>
        </div>

